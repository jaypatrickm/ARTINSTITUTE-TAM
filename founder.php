    		<h1>Robin's Story</h1>
    		<p>Robin Charles Petgrave was born in Jamaica and raised in Boston, Massachusetts by his mother. Robin was the youngest of his siblings with three sisters and one brother who passed away suddenly in 1997. While his mother worked two full-time jobs to make ends meet, Robin and his sister Florence spent three years in foster care before coming to America at age ten. People close to Robin were influenced by the wrong crowd, and he saw their lives crumble from drugs and alcohol so he became determined to avoid self destructive behaviors at all costs and to succeed. Robin grew up in Belmont, Mass where he made many lifelong friendships, explored his passion for flying by joining the Civil Air Patrol, immersed himself in extracurricular activities, and blossomed as a track star in high school. On a track scholarship, he graduated from the University of Connecticut with a Bachelors of Fine Arts Degree in Acting and a minor in Communications. He later qualified for the Olympics, but eventually moved to Los Angeles to pursue an acting career. It was in Los Angeles, that he discovered small flight schools, and obtained his FAA Commercial Helicopter Pilot License and Certified Flight Instructor’s Certificate. Shortly after, Robin started his own helicopter company, which grew to a $3.5 million a year business and earned him features in numerous books/magazines even an appearance on Oprah. He named his company Bravo Helicopters &amp; Wing because that is what everyone cheered at the end of his flights and it was based at the Torrance Airport.</p>
            <p>Robin Petgrave created Positive Vibrations in 1990 when a local school invited his first company, Bravo Helicopters &amp; Wing, for a career day landing. Then in 1992, a single mother with a troubled son met Mr. Petgrave. Robin immediately began mentoring Richard Washington, and discovered a passion for helping kids and the community. </p>
            <p>As a vehicle to motivate and mentor at-risk youth, Torrance Aeronautical Museum was founded in 1997 by Executive Director, Robin Petgrave. In 2000, Compton’s Mayor Eric Perrodin invited Robin to re-locate the organization to the Compton Airport. Seeing the greater need for at risk youth programs in Compton, Robin moved his small staff of two and a couple desks with computers to the 7,000 square foot hangar, and appropriately renamed the organization Tomorrow’s Aeronautical Museum (TAM). </p>
            <p>Today, the nonprofit museum currently boasts a variety of static display and historical artifacts, and is the umbrella agency that will be the home of the “Roots and Wings” mentoring / job training and skills development center. TAM has been a safe haven for the youth in the community. </p>
            <p>Robin and TAM have been awarded, recognized, and honored for service to at risk youth, including the General Noel F. Parrish Award presented by the National Order of Tuskegee Airmen, Inc.; the Astronaut Ronald McNair Award presented by National Black Coalition of Federal Aviation Employees; and the Living Legend Award presented by LA County Board of Supervisors. Robin and TAM have been featured in People Magazine, Los Angeles Times, Oprah, CNN, AIM Zones on AOL to name a few, and have been recognized by Senator Roderick D. Wright, Supervisor Mark Ridley-Thomas, Congresswoman Laura Richardson, and California State Assemblyman Isadore Hall. In November 2011, Robin received the Bank of America Local Heroes Award.</p>
            <p>Robin is also a highly sought after motivational speaker with “Positive Vibrations”. He is a stunt pilot with celebrity status and has several movie credits to his name. He privately funded many of the museum’s programs and equipment needs. He holds fast his belief that all children are capable of enjoying success.</p>
            <p>Robin received his BA in Acting and Communications from the University of Connecticut. He is currently a Gold Seal Flight Instructor, helicopter pilot, airplane pilot, the museum’s Executive Director, President &amp; Chief Pilot of Celebrity Helicopters, and mentor. Robin also has a wife, Myra Petgrave, a daughter, Little Robin, and son, Neeko.</p>
